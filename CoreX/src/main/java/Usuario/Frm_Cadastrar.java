    private void btn_confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmarActionPerformed
        if (jTextField1.getText().isEmpty() || jPasswordField1.getPassword().length == 0) {
            JOptionPane.showMessageDialog(rootPane, "Preencha todos os campos", "Erro", JOptionPane.WARNING_MESSAGE);
        } else {
            cliente.setNome(jTextField1.getText());
            cliente.setSenha(new String(jPasswordField1.getPassword())); // Armazena como String
            JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        }
        dispose();
    }//GEN-LAST:event_btn_confirmarActionPerformed

